package com.service.attendence.entity;

import java.io.Serializable;

/**
 * Created by yxx on 2018/4/20.
 */
public class AttendanceMonthData implements Serializable {
    private int beLate;     //迟到
    private int leaveEarly;     //早退
    private int abnormal;       //异常
    private int regular;        //正常
    private int days;           //天数
    private int unClockNum;     //未打卡
    private String personId;    //人员ID
    private String personName;  //人员name
    private String dept;         //部门
    private String transactionMon;//transactionID截取到月
    private AttendanceMonthData attendanceMonthData;

    public AttendanceMonthData() {

    }

    public AttendanceMonthData(AttendanceMonthData attendanceMonthData) {
        this.attendanceMonthData = attendanceMonthData;
    }

    public int getBeLate() {
        return beLate;
    }

    public void setBeLate(int beLate) {
        this.beLate = beLate;
    }

    public int getLeaveEarly() {
        return leaveEarly;
    }

    public void setLeaveEarly(int leaveEarly) {
        this.leaveEarly = leaveEarly;
    }

    public int getAbnormal() {
        return abnormal;
    }

    public void setAbnormal(int abnormal) {
        this.abnormal = abnormal;
    }

    public int getRegular() {
        return regular;
    }

    public void setRegular(int regular) {
        this.regular = regular;
    }

    public int getDays() {
        return days;
    }

    public void setDays(int days) {
        this.days = days;
    }

    public String getPersonId() {
        return personId;
    }

    public void setPersonId(String personId) {
        this.personId = personId;
    }

    public String getPersonName() {
        return personName;
    }

    public void setPersonName(String personName) {
        this.personName = personName;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public String getTransactionMon() {
        return transactionMon;
    }

    public void setTransactionMon(String transactionMon) {
        this.transactionMon = transactionMon;
    }

    public AttendanceMonthData getAttendanceMonthData() {
        return attendanceMonthData;
    }

    public void setAttendanceMonthData(AttendanceMonthData attendanceMonthData) {
        this.attendanceMonthData = attendanceMonthData;
    }

    public int getUnClockNum() {
        return unClockNum;
    }

    public void setUnClockNum(int unClockNum) {
        this.unClockNum = unClockNum;
    }

    @Override
    public String toString() {
        return "AttendanceMonthData{" +
                "beLate=" + beLate +
                ", leaveEarly=" + leaveEarly +
                ", abnormal=" + abnormal +
                ", regular=" + regular +
                ", days=" + days +
                ", personId='" + personId + '\'' +
                ", personName='" + personName + '\'' +
                ", dept='" + dept + '\'' +
                ", transactionMon='" + transactionMon + '\'' +
                ", attendanceMonthData=" + attendanceMonthData +
                '}';
    }
}
