package com.domain;

import java.util.Date;

public class LogDomain {

	public Integer ID;
	public String requestURL;
	public String clientIP;
	public String clientType;
	public String userID;
	public String serviceName;
	public String transName;
	public String responseCode;
	public String inputRaramMap;
	public String outputParamMap;
	public Date startTime;
	public Date endTime;
	
	public Integer getID() {
		return ID;
	}
	public void setID(Integer iD) {
		ID = iD;
	}
	public String getRequestURL() {
		return requestURL;
	}
	public void setRequestURL(String requestURL) {
		this.requestURL = requestURL;
	}
	public String getClientIP() {
		return clientIP;
	}
	public void setClientIP(String clientIP) {
		this.clientIP = clientIP;
	}
	public String getClientType() {
		return clientType;
	}
	public void setClientType(String clientType) {
		this.clientType = clientType;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getTransName() {
		return transName;
	}
	public void setTransName(String transName) {
		this.transName = transName;
	}
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public String getInputRaramMap() {
		return inputRaramMap;
	}
	public void setInputRaramMap(String inputRaramMap) {
		this.inputRaramMap = inputRaramMap;
	}
	public String getOutputParamMap() {
		return outputParamMap;
	}
	public void setOutputParamMap(String outputParamMap) {
		this.outputParamMap = outputParamMap;
	}
	public Date getStartTime() {
		return startTime;
	}
	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}
	public Date getEndTime() {
		return endTime;
	}
	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}
}
